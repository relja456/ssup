    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed
        Projekat projekatZaPretragu = new Projekat();
        projekatZaPretragu.setKompanija(this.zaposleni.getKompanija());
        
        String vrednostZaPretragu = txtPretraga.getText();
        String atributPretrage = comboElementPretrage.getItemAt(comboElementPretrage.getSelectedIndex());

        if (vrednostZaPretragu == null || vrednostZaPretragu.equals("")) {
            JOptionPane.showMessageDialog(this, "Morate uneti vrednost za pretragu!", "Upozorenje!", JOptionPane.WARNING_MESSAGE);
            return;
        }

        switch (atributPretrage) {
            case "Nazivu" ->
                projekatZaPretragu.setNaziv(vrednostZaPretragu);
            case "Opisu" ->
                projekatZaPretragu.setOpis(vrednostZaPretragu);
            case "ID-u" -> {
                try {
                    projekatZaPretragu.setIdProjekat(Integer.parseInt(vrednostZaPretragu));
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "Pogresan unos! Niste uneli celobrojnu vrednost!", "Greska!", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            }
            case "Datumu pocetka" -> {
                DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
                try {
                    LocalDate date = LocalDate.parse(vrednostZaPretragu, formatter);
                    projekatZaPretragu.setDatumPocetka(date);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Pogresan unos! Niste uneli datum u trazenom formatu (YYYY-MM-DD).", "Greska!", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            }
        }

        try {
            projekti = KlijentKontroler.getInstance().pretraziProjekte(projekatZaPretragu);
        } catch (Exception ex) {
            Logger.getLogger(PocetnaForma.class.getName()).log(Level.SEVERE, null, ex);
        }

        ptm = new ProjektiTableModel(projekti);

        if (projekti == null || projekti.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Sistem ne moze naci projekte po zadatoj vrednosti!", "Greska!", JOptionPane.ERROR_MESSAGE);
        } else {
            tblTabelaProjekata.setModel(ptm);
            JOptionPane.showMessageDialog(this, "Sistem je pronasao projekte po zadatoj vrednosti!", "Uspesna pretraga!", JOptionPane.INFORMATION_MESSAGE);
        }


    }//GEN-LAST:event_btnPretraziActionPerformed

    private void btnUcitajProjekatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUcitajProjekatActionPerformed
        int obelezenRed = tblTabelaProjekata.getSelectedRow();

        if (obelezenRed != -1) {
            try {
                Projekat ucitanProjekat = KlijentKontroler.getInstance().ucitajProjekat(projekti.get(obelezenRed));
                new FormaProjekat(ucitanProjekat, this);
            } catch (Exception ex) {
                Logger.getLogger(PocetnaForma.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Nije izabran nijedan red!", "Greska", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUcitajProjekatActionPerformed

