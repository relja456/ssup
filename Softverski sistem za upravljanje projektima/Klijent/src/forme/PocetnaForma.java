    public void popuniKomponente() {
        txtImePrezime.setText(zaposleni.getImePrezime());
        txtImeKompanije.setText(zaposleni.getKompanija().getNazivKompanije());
        txtKorisnickoIme.setText(zaposleni.getKorisnickoIme());
        txtJMBG.setText(zaposleni.getJMBG());
        txtDrzava.setText(zaposleni.getKompanija().getDrzava());
        txtGrad.setText(zaposleni.getKompanija().getGrad());
        txtDatumOsnivanja.setText(zaposleni.getKompanija().getDatumOsnivanja().toString());
        txtPIB.setText(zaposleni.getKompanija().getPIB());
        txtUloga.setText(zaposleni.getUloga().getNaziv());

        podesiStanjaUOdnosuNaDozvole();
        
        try {
            List<Zaposleni> zaposleni = KlijentKontroler.getInstance().vratiListuSvihZaposlenih(this.zaposleni);
            int brojZaposlenih = zaposleni.size();
            lblBrojTrenutnoZaposlenih.setText("Broj trenutno zaposlenih: " + String.valueOf(brojZaposlenih));
        } catch (IOException ex) {
            Logger.getLogger(PocetnaForma.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            System.out.println("Neuspesno ucitavajne zaposlenih!");
            Logger.getLogger(PocetnaForma.class.getName()).log(Level.SEVERE, null, ex);
        }

        popuniTabelu();

        comboElementPretrage.addActionListener((ActionEvent e) -> {
            txtPretraga.setText("");
        });

    }
    
    private void podesiStanjaUOdnosuNaDozvole() {
            if (!this.zaposleni.imaDozvolu(Operacija.ZAPAMTI_PROJEKAT)) {
                btnKreirajNoviProjekat.setEnabled(false);
            }
            if (!this.zaposleni.imaDozvolu(Operacija.OBRISI_PROJEKAT)){
                btnObrisiProjekat.setEnabled(false);
            }
    }

    public void popuniTabelu() {
        try {
            Projekat projekat = new Projekat();
            projekat.setKompanija(this.zaposleni.getKompanija());
            projekti = KlijentKontroler.getInstance().vratiListuSvihProjekata(projekat);

            int brojProjekata = projekti.size();
            lblBrojAktivnihProjekata.setText("Broj aktivnih projekata: " + String.valueOf(brojProjekata));

            ptm = new ProjektiTableModel(projekti);
            tblTabelaProjekata.setModel(ptm);

            tblTabelaProjekata.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        } catch (Exception ex) {
            Logger.getLogger(PocetnaForma.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void podesiLogoutNaExit() {
        WindowListener exitListener = new WindowAdapter() {

            @Override
            public void windowClosing(WindowEvent e) {
                int confirm = JOptionPane.showOptionDialog(
                        null, "Da li stvarno želite da izađete iz aplikacije?",
                        "Upozorenje", JOptionPane.YES_NO_OPTION,
                        JOptionPane.WARNING_MESSAGE, null, null, null);
                if (confirm == 0) {
                    try {
                        KlijentKontroler.getInstance().logout();
                    } catch (IOException ex) {
                        Logger.getLogger(PocetnaForma.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    System.exit(0);
                }
            }
        };
        this.addWindowListener(exitListener);
    }

