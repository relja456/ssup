    private void btnObrisiProjekatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiProjekatActionPerformed
        int obelezenRed = tblTabelaProjekata.getSelectedRow();
        if (obelezenRed == -1) {
            JOptionPane.showMessageDialog(this, "Nije izabran nijedan red!", "Greska", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Projekat projekat = projekti.get(obelezenRed);

        int dialogButton = JOptionPane.YES_NO_OPTION;
        try {
            int dialogResult = JOptionPane.showConfirmDialog(this, "Da li stvarno želite obrisati projekat: " + projekat + "?", "Upozorenje", dialogButton, JOptionPane.WARNING_MESSAGE);
            if (dialogResult == JOptionPane.YES_OPTION) {
                KlijentKontroler.getInstance().obrisiProjekat(projekat);
                popuniTabelu();
                JOptionPane.showMessageDialog(this, "Uspesno obrisan projekat: " + projekat + "!", "Informacija", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Greska prilikom brisanja projekta: " + projekat + "!", "Greska", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(PocetnaForma.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Greska prilikom brisanja projekta: " + projekat + "!", "Greska", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(PocetnaForma.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnObrisiProjekatActionPerformed

